#include "fillit.h"

void	ft_erase(int i, t_map *m)
{
	int x;
	int y;
	int count;

	x = 0;
	y = 0;
	count = 0;
	while (count < 4)
	{
		if (m->map[x][y] == i)
		{
			m->map[x][y] = '.';
			count++;
			x++;
			if (m->map[x][y] == 0)
			{
				x = 0;
				y++;
			}
		}
	}
}
